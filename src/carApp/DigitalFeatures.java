package carApp;

public class DigitalFeatures {
	String seat, roofType, backCamera, navigation, bluetooth, remoteStart, pushStart, monitor, voiceControl, autoParking;
	
	public DigitalFeatures(String seat, String roofType, String backCamera, String navigation, String bluetooth, 
			String remoteStart, String pushStart, String monitor, String voiceControl, String autoParking) {
		
		this.seat = seat;
		this.roofType = roofType;
		this.backCamera = backCamera;
		this.navigation = navigation;
		this.bluetooth = bluetooth;
		this.remoteStart = remoteStart;
		this.pushStart = pushStart;
		this.monitor = monitor;
		this.voiceControl = voiceControl;
		this.autoParking = autoParking;
	}
}
